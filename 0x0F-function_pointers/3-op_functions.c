#include "3-calc.h"

/**
 * op_add - function calculating sum
 * @a: input 1
 * @b: input 2
 *
 * Return: return a+b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function calculating difference
 * @a: input 1
 * @b: input 2
 * Return: return a-b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op-mul - function calculating multiplication
 * @a: input 1
 * @b: input 2
 * Return: return a*b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function calculating division
 * @a: input 1
 * @b: input 2
 * Return: return a/b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op-mod - function calculating modulus
 * @a: input 1
 * @b: input 2
 * Return: return a%b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
