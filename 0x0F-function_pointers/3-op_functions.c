#include "3-calc.h"
/**
 * op_add - function responsible for addition
 * @a: first number
 * @b: second number
 *
 * Return: return sum of the 2 values
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function responsible for subtraction
 * @a: first number
 * @b: second number
 *
 * Return: return subrtaction of the 2 numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function responsible for multiplicaton
 * @a: first number
 * @b: second number
 *
 * Return: return multiplication of the 2 numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function responsible for division
 * @a: first number
 * @b: second number
 *
 * Return: return division of the 2 numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function responsible for modulus
 * @a: first number
 * @b: second number
 *
 * Return: return modulus of the 2 numbers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
